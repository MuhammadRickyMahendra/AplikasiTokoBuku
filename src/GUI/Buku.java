    private void autonumber(){
        try {
            Connection c = koneksi.getKoneksi();
            Statement s = c.createStatement();
            String sql = "SELECT * FROM buku ORDER BY ID_Buku DESC";
            ResultSet r = s.executeQuery(sql);
            if (r.next()) {
                String Id_buku = r.getString("ID_Buku").substring(2);
                String BK = "" +(Integer.parseInt(Id_buku)+1);
                String Nol = "";
                
                if (BK.length()==1)
                {Nol = "00";}
                else if (BK.length()== 2)
                {Nol = "0";}
                else if(BK.length()== 3)
                {Nol = "";}
                
                txtidbuku.setText("BK" + Nol + BK);
            }else{
                txtidbuku.setText("BK001");
            }
            r.close();
            s.close();
        } catch (Exception e) {
            System.out.println("autonumber error");
        }
    }
    
